package Activity_5;

// 1. Write a program to print numbers from 1 to 10.
public class Number_1 {
    public static void main(String[] args) {
        // Print a header message to indicate what the program does
        System.out.println("Printing numbers from 1 to 10:");

        // Initialize loop counter i to 1, continue while i <= 10, increment i by 1 each time
        for (int i = 1; i <= 10; i++) {
            // Print the current value of i followed by a newline
            System.out.println(i);
        }
    }
}
